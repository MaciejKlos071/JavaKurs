package JavaPodstawy.HomeWork.HomeWork_5.podstawy;

public class OperatoryLogiczne {
    public static void main(String[] args) {
        boolean firstValue = true;
        boolean secoundValue = false;
        boolean thirdValue = true;
        boolean forthValue = false;

        System.out.println(firstValue && secoundValue);
        System.out.println(firstValue && thirdValue);
        System.out.println(firstValue || secoundValue);
        System.out.println(secoundValue || forthValue);
        System.out.println(!firstValue);
        System.out.println(!secoundValue);
        System.out.println(!firstValue && secoundValue);

    }

}
